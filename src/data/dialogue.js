export function pick(arr, avoid) {
  if (!arr || arr.length === 0) return ""
  if (arr.length === 1) return arr[0]
  let line = arr[Math.floor(Math.random() * arr.length)]
  let guard = 0
  while (line === avoid && guard < 5) {
    line = arr[Math.floor(Math.random() * arr.length)]
    guard++
  }
  return line
}
